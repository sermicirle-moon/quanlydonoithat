    private void loadNhaCungCap() throws SQLException {
        nhacungcapDAO dao = new nhacungcapDAO(dbconnection.getConnection());
        List<nhacungcap> listncc = dao.getAll();
        DefaultTableModel model = (DefaultTableModel) tblnhacc.getModel();
        model.setRowCount(0);
        for (nhacungcap x : listncc) {
            model.addRow(new Object[]{
                x.getMancc(),
                x.getTenncc(),
                x.getDiachincc(),
                x.getSdtncc()
            });
        }
    }

