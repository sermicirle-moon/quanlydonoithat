    private void btnthemnccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemnccActionPerformed
        String ma = txtmancc.getText().trim();
        String ten = txttenncc.getText().trim();
        String diachi = txtdiachincc.getText().trim();
        String sdt = txtsdtncc.getText().trim();

        if (validate.isEmpty(ma) || validate.isEmpty(ten)
                || validate.isEmpty(diachi) || validate.isEmpty(sdt)) {
            JOptionPane.showMessageDialog(this, "Không được để trống bất kỳ trường nào!");
            return;
        }
        if (!validate.isPositiveInteger(ma)) {
            JOptionPane.showMessageDialog(this, "Mã nhà cung cấp phải là số nguyên dương!");
            return;
        }
        if (!validate.isValidPhoneNumber(sdt)) {
            JOptionPane.showMessageDialog(this, "nhập đúng định dạng của số điện thoại");
            return;
        }
        nhacungcap ncc = new nhacungcap();
        ncc.setMancc(Integer.parseInt(ma));
        ncc.setTenncc(ten);
        ncc.setDiachincc(diachi);
        ncc.setSdtncc(sdt);
        try {
            nhacungcapDAO dao = new nhacungcapDAO(dbconnection.getConnection());
            if (dao.findById(ncc.getMancc()) != null) {
                JOptionPane.showMessageDialog(this, "Mã nhà cung cấp đã tồn tại!");
                return;
            }
            boolean success = dao.insert(ncc);
            if (success) {
                JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thành công!");
                loadNhaCungCap();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại!");
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi SQL: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_btnthemnccActionPerformed

    private void btnsuanccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuanccActionPerformed
        String ma = txtmancc.getText().trim();
        String ten = txttenncc.getText().trim();
        String diachi = txtdiachincc.getText().trim();
        String sdt = txtsdtncc.getText().trim();
        int selectedRow = tblnhacc.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 nhà cung cấp để sửa");
            return;
        }
        if (validate.isEmpty(ma) || validate.isEmpty(ten) || 
            validate.isEmpty(diachi) || validate.isEmpty(sdt)) {
            JOptionPane.showMessageDialog(this, "Không được để trống trường nào!");
            return;
        }
        if (!validate.isPositiveInteger(ma)) {
            JOptionPane.showMessageDialog(this, "Mã nhà cung cấp phải là số nguyên dương!");
            return;
        }
        if (validate.isValidPhoneNumber(sdt)) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ (9–11 số)!");
            return;
        }
        if (!ten.matches("[\\p{L}\\s]+")) {
            JOptionPane.showMessageDialog(this, "Tên nhà cung cấp không được chứa số hoặc ký tự đặc biệt!");
            return;
        }

        try {
            nhacungcapDAO dao = new nhacungcapDAO(dbconnection.getConnection());
           
            nhacungcap ncc = new nhacungcap();
            ncc.setMancc(Integer.parseInt(ma));
            ncc.setTenncc(ten);
            ncc.setDiachincc(diachi);
            ncc.setSdtncc(sdt);

            boolean success = dao.update(ncc);

            if (success) {
                JOptionPane.showMessageDialog(this, "Cập nhật nhà cung cấp thành công!");
                loadNhaCungCap();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi SQL: " + e.getMessage());
        }
    }//GEN-LAST:event_btnsuanccActionPerformed

