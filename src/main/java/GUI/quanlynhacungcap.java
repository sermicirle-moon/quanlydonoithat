    private void btnxoanccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoanccActionPerformed
        int[] selectedRows = tblnhacc.getSelectedRows();
        if (selectedRows.length == 0) {
            JOptionPane.showMessageDialog(this, "Hãy chọn ít nhất 1 nhà cung cấp để xóa");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Bạn có chắc muốn xóa nhà cung cấp không?",
                "Xác nhận xóa",
                JOptionPane.YES_NO_OPTION
        );
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblnhacc.getModel();
        List<Integer> listMaNCC = new ArrayList<>();
        for (int i : selectedRows) {
            int ma = Integer.parseInt(model.getValueAt(i, 0).toString());
            listMaNCC.add(ma);
        }
        nhacungcapDAO dao= new nhacungcapDAO(dbconnection.getConnection());
        for (int ma : listMaNCC) {
        try {
                dao.delete(ma);
                loadNhaCungCap();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Không thể xóa nhà cung cấp có mã: " + ma);
            }
        }
        clear();
    }//GEN-LAST:event_btnxoanccActionPerformed


